    /**
     * display file info
     * @param myFileInfo
     */
    public void displayFileInfo(FileInfoInt myFileInfo) {
		try {
			PanelCover coverImg = (PanelCover) jPanelPlayerCover;
			coverImg.setImage(myFileInfo.getCoverImage(), true);
		} catch (Exception ex) {
			Popup.error(ex);
		}
	}
    
