    /**
     * display MP3 play position in slider
     * @param currentPosition
     */
    public void dispMP3progress(int currentPosition) {
        jSliderPlayerLength.setValue(currentPosition);
		jLabelPlayerTimeEllapsed.setText(StringManager.secondsToMMSS(currentPosition));
    }
    
    /**
     * display total time
     * @param maximum
     */
    public void setMax(int maximum) {
        jSliderPlayerLength.setMaximum(maximum);
		jLabelPlayerTimeTotal.setText(StringManager.secondsToMMSS(maximum));
    }
    
